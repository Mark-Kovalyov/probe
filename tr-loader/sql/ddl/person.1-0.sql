     is_tenure_in text,
     from_institution_name text,
     with_degree text,
     in_subject text,
     has_qualification text,
     additional_name text,
     family_name text,
     given_name text,
     has_tenure_in_organization text,
     holds_position text,
     has_gender text,
     honorific_prefix text,
     honorific_suffix text,
     preferred_name text,
     date_of_death text,
